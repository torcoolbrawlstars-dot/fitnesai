export interface AnalyzeResponse {
  source: "gemini" | "fallback";
  note?: string;
  result: FullResult;
}

export async function analyzePhoto(image: string, profile: Profile, lang: "ru" | "en" = "ru"): Promise<AnalyzeResponse> {
  const match = /^data:(image\/[a-z0-9+.-]+);base64,(.+)$/i.exec(image);
  if (!match) throw new Error("Загрузите фотографию");

  if (KEY) {
    try {
      const r = await fetch(
        `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key=${KEY}`,
        {
          method: "POST",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({
            contents: [
              {
                parts: [
                  { text: buildPrompt(profile, lang) },
                  { inline_data: { mime_type: match[1], data: match[2] } },
                ],
              },
            ],
            generationConfig: {
              response_mime_type: "application/json",
              temperature: 0.4,
              maxOutputTokens: 16384,
              thinkingConfig: { thinkingBudget: 0 },
            },
          }),
          signal: AbortSignal.timeout(60_000),
        }
      );

      if (r.ok) {
        const data = await r.json();
        const text: string | undefined = data?.candidates?.[0]?.content?.parts?.[0]?.text;
        if (text) {
          /* иногда модель заворачивает JSON в ```json``` — вырезаем */
          const clean = text.replace(/^```(?:json)?/m, "").replace(/```\s*$/m, "").trim();
          const parsed = JSON.parse(clean) as FullResult;
          return { source: "gemini", result: parsed.valid === false ? parsed : { ...parsed, valid: true } };
        }
      }
      console.error("Gemini error:", r.status);
    } catch (e) {
      console.error("Gemini request failed:", e);
    }
  }

  return {
    source: "fallback",
    note:
      lang === "en"
        ? "AI service is unavailable. Showing a calculation based on your data without photo analysis."
        : "AI-сервис недоступен. Показан расчёт по вашим данным без анализа фото.",
    result: fallbackResult(profile),
  };
}

/* ---------- AI-коуч: чат с Gemini о себе, тренировках и прогрессе ---------- */

export interface ChatMessage {
  role: "user" | "model";
  text: string;
}

export async function coachChat(
  history: ChatMessage[],
  profile: Profile,
  latest: FullResult | undefined,
  lang: "ru" | "en" = "ru"
): Promise<string> {
  if (!KEY) throw new Error(lang === "en" ? "AI service unavailable" : "AI-сервис недоступен");

  const genderRu = profile.gender === "female" ? "женский" : "мужской";
  const system = `Ты — персональный AI-фитнес-коуч сервиса BodyVision AI. Общайся дружелюбно, мотивирующе и конкретно, как опытный тренер. Отвечай кратко (3-6 предложений), без воды. ${
    lang === "en" ? "ALWAYS reply in English." : "Всегда отвечай на русском языке."
  }

ДАННЫЕ КЛИЕНТА:
- Пол: ${genderRu}, возраст: ${profile.age}, рост: ${profile.height} см, вес: ${profile.weight} кг
- Тренируется: ${profile.location === "gym" ? "в зале" : "дома"}
- Выбранный режим: ${
    profile.goalMode === "lose"
      ? "похудение"
      : profile.goalMode === "gain"
      ? "набор массы"
      : profile.goalMode === "both"
      ? "похудение + масса (рекомпозиция)"
      : "авто"
  }
${
  latest
    ? `ПОСЛЕДНИЙ AI-АНАЛИЗ ТЕЛА:
- Цель: ${latest.goal ?? "-"}
- Жир: ${latest.bodyFat}%, мышцы: ${latest.muscle}%, осанка: ${latest.posture}/100, общий балл: ${latest.score}/100
- Слабые места: ${latest.weaknesses.join(", ")}
- Сильные стороны: ${latest.strengths.join(", ")}`
    : "Анализ тела ещё не проводился — предложи загрузить фото для точных рекомендаций."
}

Помогай улучшать тело: тренировки, питание, восстановление, мотивация, техника упражнений. На вопросы не о фитнесе и здоровье мягко возвращай разговор к целям клиента. Не ставь медицинские диагнозы — при жалобах на боль советуй врача.`;

  const r = await fetch(
    `https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key=${KEY}`,
    {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        system_instruction: { parts: [{ text: system }] },
        contents: history.slice(-20).map((m) => ({
          role: m.role,
          parts: [{ text: m.text }],
        })),
        generationConfig: {
          temperature: 0.7,
          maxOutputTokens: 2048,
          thinkingConfig: { thinkingBudget: 0 },
        },
      }),
      signal: AbortSignal.timeout(45_000),
    }
  );

  if (!r.ok) throw new Error(`Gemini ${r.status}`);
  const data = await r.json();
  const text: string | undefined = data?.candidates?.[0]?.content?.parts?.[0]?.text;
  if (!text) throw new Error("empty response");
  return text.trim();
}
